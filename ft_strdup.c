#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*aux;
	int		i;

	aux = malloc(sizeof(char) * ft_strlen(src) + 1);
	if (!(aux))
	{
		return (0);
	}
	i = 0;
	while (src[i] != '\0')
	{
		aux[i] = src[i];
		i++;
	}
	aux[i] = '\0';
	return (aux);
}
